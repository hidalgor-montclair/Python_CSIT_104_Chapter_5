# This program says "Hi" to each person in a list of names.

# Step 1: We have a list of names: Bill, Nicole, and John.
# The loop will go through each name one by one.
for name in ['Bill', 'Nicole', 'John']:
    
    # Step 2: Inside the loop, we print a greeting for each name.
    # The f-string helps us add the name directly into the message.
    print(f'Hi {name}!')  # This prints a personalized greeting for each name.

