i = 0  # Initialize the variable 'i' with a value of 0.

# The while loop will continue running as long as 'i' is less than 100.
while i < 100:
    print(i)  # Print the current value of 'i'.
    
    # Increment 'i' by 1. This is a shorthand for i = i + 1.
    # It ensures 'i' gets larger and eventually reaches 100, stopping the loop.
    i += 1  
